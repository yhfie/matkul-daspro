#include <iostream>

using namespace std;

int main()
{
    int a, b;
    float c;

    a = 10; //Input bilangan pertama
    b = 5; //Input bilangan kedua
    c = float (a+b)/2; //Code rumus

    cout << "Isi variabel a = " <<a<<endl; //Output bilangan pertama
    cout << "Isi variabel b = " <<b<<endl; //Output bilangan kedua
    cout << "Isi variabel c = " <<c<<endl; //Output hasil perhitungan
    return 0;
}
